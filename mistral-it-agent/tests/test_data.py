class TestEscalationPolicy:

    def test_escalation_policy_covers_all_intents(self, data_files):
        policy = data_files["escalation_policy"]
        covered = {p["intent"] for p in policy["escalation_paths"]}
        missing = INTENT_LABELS - covered
        assert not missing, (
            f"escalation_policy.json is missing entries for: {missing}"
        )

    def test_escalation_policy_each_path_has_tiers(self, data_files):
        for path in data_files["escalation_policy"]["escalation_paths"]:
            assert "tiers" in path, f"No tiers for intent '{path['intent']}'"
            assert len(path["tiers"]) >= 1, (
                f"Intent '{path['intent']}' must have at least one escalation tier"
            )

    def test_escalation_policy_tiers_have_contact(self, data_files):
        for path in data_files["escalation_policy"]["escalation_paths"]:
            for tier in path["tiers"]:
                assert "contact" in tier or "slack" in tier, (
                    f"Tier {tier.get('tier')} of '{path['intent']}' has no contact info"
                )

    def test_escalation_policy_payments_incident_is_always_p1(self, data_files):
        """The escalation policy must document that all payments incidents are P1."""
        payments = next(
            p for p in data_files["escalation_policy"]["escalation_paths"]
            if p["intent"] == "payments_incident"
        )
        sla_text = json.dumps(payments.get("sla_overrides", {}))
        assert "P1" in sla_text


# ---------------------------------------------------------------------------
# JSONL split files (generated by 01_data_prep.ipynb — may not exist)
# ---------------------------------------------------------------------------

class TestJsonlSplits:

    @pytest.mark.parametrize("filename", ["train.jsonl", "val.jsonl", "test.jsonl"])
    def test_jsonl_splits_if_exist(self, filename):
        path = _DATA_DIR / filename
        if not path.exists():
            pytest.skip(f"{filename} not yet generated — run 01_data_prep.ipynb first")

        with open(path) as f:
            lines = [line.strip() for line in f if line.strip()]

        assert len(lines) > 0, f"{filename} is empty"

        for i, line in enumerate(lines, start=1):
            try:
                record = json.loads(line)
            except json.JSONDecodeError as exc:
                pytest.fail(f"{filename} line {i} is not valid JSON: {exc}")

            assert "text" in record, (
                f"{filename} line {i} missing 'text' key"
            )
            assert "labels" in record, (
                f"{filename} line {i} missing 'labels' key"
            )
            assert "intent" in record["labels"], (
                f"{filename} line {i} missing 'labels.intent' key"
            )
            assert record["labels"]["intent"] in INTENT_LABELS, (
                f"{filename} line {i} has unknown intent '{record['labels']['intent']}'"
            )
